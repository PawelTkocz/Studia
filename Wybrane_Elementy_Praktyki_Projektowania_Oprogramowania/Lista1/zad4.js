function fibonacci_iter(n){
    if(n < 1){
        return 0;
    }
    let a = 0;
    let b = 1;
    for(let i=1; i<n; i++){
        [a, b] = [b, a+b];
    }
    return b;
}

function fibonacci_recursive(n){
    if(n <= 1){
        return n;
    }
    return fibonacci_recursive(n-1)+fibonacci_recursive(n-2);
}

function measure_time_iter(max_n){
    console.log("Iterative");
    for(let n=10; n<=max_n; n+=1000000){
        let time_taken = n.toString();
        console.time(time_taken);
        fibonacci_iter(n);
        console.timeEnd(time_taken);
    }
}

function measure_time_recursive(max_n){
    console.log("Recursive");
    for(let n=10; n<=max_n; n++){
        let time_taken = n.toString();
        console.time(time_taken);
        fibonacci_recursive(n);
        console.timeEnd(time_taken);
    }
}

